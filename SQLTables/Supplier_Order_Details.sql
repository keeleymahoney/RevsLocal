/*
Create table called Supplier_Order_Details with attributes
    ID as primary key (serial increment)
    Order_Number Int (which references Supplier_Orders Order_Number)
    Ingredient Text (whcih references Ingredients Ingredient_Name)
    Quantity Ordered Int, which is the number of an ingredient that is ordered
    Quantity received Int, which is the number of an ingredient that is received
    Received boolean, which dictates if an order was receieved or not
    Wholesale_Unit_PRice decimal, which is the wholesale_unit_price from the supplier
*/

CREATE TABLE Supplier_Order_Details(
    ID SERIAL PRIMARY KEY,
    Order_Number INT,
    Ingredient TEXT,
    Quantity_Ordered INT,
    Quantity_Received INT,
    Received BOOLEAN,
    Wholesale_Unit_Price DECIMAL,
    FOREIGN KEY (Order_Number) REFERENCES Supplier_Orders(Order_Number),
    FOREIGN KEY (Ingredient) REFERENCES ingredients(ingredient_name)
);

